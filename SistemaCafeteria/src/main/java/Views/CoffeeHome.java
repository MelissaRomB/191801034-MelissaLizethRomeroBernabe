    private void TotalPagar(){
        totalPagar = 0.00;
        int numFila = TableVentas.getRowCount();
        for(int i = 0; i < numFila; i++){
            double calcular = Double.parseDouble(String.valueOf(TableVentas.getModel().getValueAt(i,5)));
            totalPagar = totalPagar + calcular;
        }
        LabelTotarPagar.setText(String.format("%.2f", totalPagar));
    }
