    private void txtCodigoNVKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoNVKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtCodigoNV.getText())){
                String codigo = txtCodigoNV.getText();
                prod = daoProd.BuscarProductos(codigo);
                if(prod.getNombrePro() != null){
                    txtNomProdNV.setText(""+prod.getNombrePro());
                    txtPrecioNV.setText(""+prod.getPrecio());
                    txtMedidaNV.setText(""+prod.getMedida());
                    txtCantidadNV.requestFocus();
                }
                else{
                    LimpiarVenta();
                    txtCodigoNV.requestFocus();
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Ingrese el codigo del producto");
                txtCodigoNV.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCodigoNVKeyPressed

