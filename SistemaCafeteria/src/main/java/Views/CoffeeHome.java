    private void txtCantidadNVKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadNVKeyPressed
         if(evt.getKeyCode()== KeyEvent.VK_ENTER){
            if(!"".equals(txtCantidadNV.getText())){
                String codigo = txtCodigoNV.getText();
                String nombre = txtNomProdNV.getText();
                int cantidad = Integer.parseInt(txtCantidadNV.getText());
                double precio = Double.parseDouble(txtPrecioNV.getText());
                String medida = txtMedidaNV.getText();
                double total = cantidad*precio;
                item = item +1;
                DefaultTableModel model = (DefaultTableModel) TableVentas.getModel();
                for(int i = 0; i < TableVentas.getRowCount(); i++){
                    if(TableVentas.getValueAt(i, 1).equals(txtNomProdNV.getText())){
                        JOptionPane.showMessageDialog(null, "El producto ya esta registrado en la venta");
                        return;
                    }
                }
                ArrayList lista = new ArrayList();
                lista.add(item);
                lista.add(codigo);
                lista.add(nombre);
                lista.add(cantidad);
                lista.add(precio);
                lista.add(medida);
                lista.add(total);
                Object[] ob = new Object[6];
                ob[0] = lista.get(1);
                ob[1] = lista.get(2);
                ob[2] = lista.get(3);
                ob[3] = lista.get(4);
                ob[4] = lista.get(5);
                ob[5] = lista.get(6);
                model.addRow(ob);
                TableVentas.setModel(model);
                TotalPagar();
                LimpiarVenta();
                txtCodigoNV.requestFocus();
            }
            else{
                JOptionPane.showMessageDialog(null,"Ingrese una cantidad");
            }
        }
    }//GEN-LAST:event_txtCantidadNVKeyPressed

