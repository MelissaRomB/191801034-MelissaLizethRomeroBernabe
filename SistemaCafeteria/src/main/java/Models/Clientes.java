package Models;

/**
 *
 * @author USUARIO
 */
public class Clientes {
    private String nombre, direccion, estado, telefono;
    private int id;

    public Clientes() {
    }

    public Clientes(String nombre, String direccion, String estado, String telefono, int id) {
        this.nombre = nombre;
        this.direccion = direccion;
        this.estado = estado;
        this.telefono = telefono;
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
